let month="August";

if(month==="September" || month==="October" || month==="November" ){
    console.log('Autumn');
}
else if(month==="December" || month==="February" || month==="January"){
    console.log('Winter');
}
else if(month==="March" || month==="April" || month=="May"){
    console.log('Spring');
}
else{
    console.log('Summer');
}